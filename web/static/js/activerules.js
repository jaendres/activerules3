/**
 * Display ther active_modal.
 * The div with the ID of active_modal is used through ActiveRules.
 * It is present on almost all pages, unless excluded.
 */
function active_modal(requested_content)
{
    //alert($requested_content);
    $('#active_modal').jqm({ajax: requested_content});
    $('#active_modal').jqmShow();
}

function close_active_modal()
{
    //alert($requested_content);
    $('#active_modal').jqmHide();
}

/**
 * Submit a form via Ajax
 * Display thr resulting HTML in the active_modal div
 * The HTML returned may have style overrides for the active_modal.
 */
function active_form_submit(form)
{
    var data = $("#"+form.id).serialize();

   $.ajax({
        type: form.method,
        url: form.action,
        data:  data,
        success: function(data) {
            //display message back to user here
            $('#active_modal').html(data);
          }  ,
        dataType: 'html'
    });
}

